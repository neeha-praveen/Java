public class vowel_or_consonant {
    public static void main(String[] args) {
        char x = 't';
        if (x== 'a'|x=='e'|x=='i'|x=='o'|x=='u'){
            System.out.println("char is a vowel");
        }
        else {
            System.out.println("char is a consonant");
        }
    }
}
